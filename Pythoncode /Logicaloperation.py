# Define variables to represent weather conditions
its_sunny = True
has_umbrella = False

# Check if it's sunny and the person doesn't have an umbrella
if its_sunny and not has_umbrella:
    # If both conditions are true, print a message indicating it's perfect weather
    print("Perfect weather to go outside and enjoy the sunlight")
else:
    # If either it's not sunny or the person has an umbrella, print a different message
    print("Better stay indoors. Who needs an umbrella, anyway? It's just a passing cloud.")
